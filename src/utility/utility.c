/*************************************************************************************************************
* @Param[in]  : num -- 需要转换的整数
* @Param[out] : str -- 存放整数字符串的数组指针
* @Return     : 整数num的位数
* @Description: 将一个整数转为字符串
* @History    :
* -----------------------------------------------------------------------------------------------------------
* @Date         Version    Author        Modify 
* ----------------------------------------------------------------------------------------------------------- 
* 2015-01-27    V1.0       Kevin Zhou    Initial version create
*************************************************************************************************************/
int my_itoa(int num, char *str)
{
    char   *p = str;
    int i, j;
    int power = 0;

    j = num;
    for (power = 1; j>10; j /= 10)
    {
        power *= 10;
    }

    j = num;
    for (i=0; power>0; power /= 10)
    {
        *p++ = '0' + j / power;
        j %= power;
        i++;
    }
    *p = '\0';
    // printf("num=%d, str=%s\n", num, str);
    
    return i;
}

